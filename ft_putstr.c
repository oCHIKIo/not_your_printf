#include "ft_printf.h"

int	ft_putstr(char *ptr)
{
	int	i;

	i = 0;
	if (!ptr)
		return (write(1, "(null)", 6));
	while (ptr[i])
	{
		ft_putchar(ptr[i]);
		i++;
	}
	return (i);
}
